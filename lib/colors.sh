#!/bin/bash

# ======================================================
# Hadoop Auto Installer v2.0
# Colors Library
# ======================================================

# Reset
NC="\033[0m"

# Regular Colors
BLACK="\033[0;30m"
RED="\033[0;31m"
GREEN="\033[0;32m"
YELLOW="\033[0;33m"
BLUE="\033[0;34m"
PURPLE="\033[0;35m"
CYAN="\033[0;36m"
WHITE="\033[0;37m"

# Bold Colors
BOLD="\033[1m"
BRED="\033[1;31m"
BGREEN="\033[1;32m"
BYELLOW="\033[1;33m"
BBLUE="\033[1;34m"
BPURPLE="\033[1;35m"
BCYAN="\033[1;36m"
BWHITE="\033[1;37m"

# ======================================================
# Banner
# ======================================================

banner() {
clear
echo -e "${BCYAN}"
echo "==============================================================="
echo "             Hadoop Auto Installer v2.0"
echo "==============================================================="
echo -e "${NC}"
}

# ======================================================
# Messages
# ======================================================

info() {
    echo -e "${BBLUE}[INFO]${NC} $1"
}

success() {
    echo -e "${BGREEN}[SUCCESS]${NC} $1"
}

warning() {
    echo -e "${BYELLOW}[WARNING]${NC} $1"
}

error() {
    echo -e "${BRED}[ERROR]${NC} $1"
}

title() {
    echo
    echo -e "${BCYAN}===============================================================${NC}"
    echo -e "${BWHITE}$1${NC}"
    echo -e "${BCYAN}===============================================================${NC}"
}

section() {
    echo
    echo -e "${BPURPLE}------------------- $1 -------------------${NC}"
}
